void main(List<String> args) {
  /// 4 types of operators
  /// unary prefix, unary postfix, binary infix, compound assignment
  int age = 30;
  print(--age);
  const myTrue = true;
  print(!myTrue);
  print(++age);
  // unary bitweese complement prefix operator
  print(~1);
  print(-age);
  print(age);
}
